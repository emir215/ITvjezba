public class HelloWorld {
    public static void main(String[] args) {
        // Broj puta koji zelimo ponoviti poruku
        int brojPonavljanja = 36;

        // Petlja koja ce ispisati poruku Hello World odredeni broj puta
        for (int i = 0; i < brojPonavljanja; i++) {
            System.out.println("Hello World");
        }
    }
}
